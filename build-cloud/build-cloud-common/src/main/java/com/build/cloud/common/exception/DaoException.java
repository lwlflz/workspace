package com.build.cloud.common.exception;
/**
 * <p>
 * DaoException.java
 * </p>
 * <p>
 * Dao层异常类
 * </p>
 * Dao层代码统一抛出的异常类，Service层必须对此类异常进行捕获和处理。
 */
public class DaoException extends BusinessException {
	private static final long serialVersionUID = -7645633187085897783L;
	public DaoException(String message) {
		super(message);
	}
	public DaoException(String message, Throwable cause) {
		super(message, cause);
	}
}
